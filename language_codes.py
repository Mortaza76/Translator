def extract_base_code(locale_code: str) -> str:
    """
    Extract the base language code from a locale code.
    
    Examples:
        'en-US' -> 'en'
        'es-ES' -> 'es'
        'zh-Hans' -> 'zh'
        'fr' -> 'fr'
    
    Args:
        locale_code: Locale code (e.g., 'en-US', 'es', 'zh-Hans')
        
    Returns:
        Base language code
    """
    # Split by '-' and take the first part
    return locale_code.split('-')[0]


def is_supported_locale(locale_code: str) -> bool:
    """
    Check if a locale code is supported.
    
    Args:
        locale_code: Locale code to check
        
    Returns:
        True if the locale code is supported
    """
    return locale_code in SUPPORTED_LOCALE_CODES


def get_language_name_from_code(code: str) -> str:
    """
    Get language name from language code.
    
    Args:
        code: Language code (e.g., 'en', 'es', 'fr')
        
    Returns:
        Language name or the code itself if not found
    """
    base_code = extract_base_code(code)
    return CODE_TO_LANGUAGE_NAME.get(base_code, code.capitalize())
